					<p>Generación Hip-Hop está destinado a ser, al menos en lengua castellana, el centro de un macrocosmos historiográfico sobre un estilo musical que germinó con la influencia estética y política del reggae de Bob Marley –“que, hacia fines de los 70, se encargaba de mantener económicamente a seis mil personas”–, maduró bajo la crisis social y racial de las comunidades negras en los más importantes centros urbanos estadounidenses de los años setenta y ochenta –“tenemos conciencia de que todos somos hermanos, que vivimos en los mismos barrios y afrontamos las mismas dificultades”–, y construyó lazos serios con la industria musical en los 90 –en versión rap y gangsta rap, con mixtapes y compact discs– para fusionarse más tarde con sonidos y estilos de casi todos los países, las razas y los idiomas del mundo. Con este recorrido en cuenta, la investigación de Jeff Chang empieza en 1968 y termina en 2001, y durante ese período su mirada sobre el hip-hop recorre voces, documentos, coyunturas políticas y sociales pero, sobre todo, una vasta discografía a través de la cual la historia del sonido no puede dejar de escucharse como una historia –y una crítica– de la cultura afroamericana contemporánea.</p>
				
