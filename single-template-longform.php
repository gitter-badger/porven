						<p>Como ocurre con otros géneros musicales, la vida y el sentido del hip-hop no pueden asimilarse sin la consideración de su geografía. “El blues tuvo al Mississippi, el jazz tuvo a Nueva Orleans. El hip-hop tiene a Jamaica”, explica Jeff Chang como punto inicial de su libro. Entre las decenas de personajes que Chang entrevistó para Generación Hip-Hop (Caja Negra), el disc-jockey y productor musical Kool Herc –que a los 15 años llegó desde las Grandes Antillas al Bronx de Nueva York con una herencia musical a primera vista incompatible con los compartimentos de la cultura estadounidense– es quien mejor sintetiza la importancia del reggae jamaiquino no sólo como sonido base del hip-hop, sino como plataforma para sus múltiples aspiraciones políticas.</p>
					<p>Durante los 70, después de la independencia de Jamaica de Gran Bretaña y en plena crisis nacional, figuras como Bob Marley recurrieron a la música para volver compatible desde la fuerza de la cultura popular lo que la política institucional parecía incapaz de resolver. Palabras como “gueto” y “violencia” empezaron a relacionarse desde esa época –para evolucionar varias veces a lo largo de las distintas etapas del hip-hop– con las urgencias y las nuevas expresiones de la comunidad negra. La migración de jamaiquinos, portorriqueños y otras minorías hacia los guetos de predominancia afroamericana en Nueva York no iba a tardar en transformarse en un nuevo estilo y en marcar a una generación. En el prólogo del libro de Chang, DJ Kool Herc romantiza esa vieja impronta: “Lo importante no es la ostentación. Lo importante no es la cantidad de balas que dispara tu arma, ni usar zapatillas de doscientos dólares, ni ver si soy mejor que tú o si tú eres mejor que yo. Lo importante es la relación entre nosotros, conectarse el uno con el otro. Por eso tiene un atractivo especial. Ha dado a los jóvenes una manera de entender su propio mundo, vivan en los suburbios, en la ciudad o donde sea”.</p>
