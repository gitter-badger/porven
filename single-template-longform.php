					<p>Por su lado, el de finales de los 70 en EE.UU. sin dudas era un mundo en el que las conexiones comunitarias necesitaban algo que reafirmara su sentido. La crisis económica ligada al petróleo y las drásticas reformas del mercado inmobiliario en Nueva York le dieron al segregacionismo un tono que las pandillas criminales oscurecieron más y al que movimientos “de pacificación social” como la Universal Zulu Nation –alrededor de la que orbitaban pioneros del hip-hop como Grandmaster Flash y Afrika Bambaataa– intentaban contrarrestar fundando sellos discográficos. En ese punto, los grafitis que empezaron a inundar el espacio público a través de paredes, subtes y trenes se consolidaron como un arte –de autores míticos como TAKI 183 y EVIL ED– con el objetivo de servir “como un ataque contra la invisibilidad de su generación y una manera de prepararse para el período de oscuridad que se avecinaba”, explica Chang. El Estados Unidos blanco y resplandeciente de Ronald Reagan, a partir de 1980, fue por eso un punto clave en la historia del hip-hop. Artistas y promotores como Jazzy Jay y Kool Lady Blue, y más tarde raperos como Rum-DMC, le dieron al Bronx un nuevo estilo y la efervescencia de una nueva identidad, marcada por letras, rimas, bailes y sonidos que no iban a tardar en inquietar a quienes vivían en la más plácida Manhattan.</p>
					
					
